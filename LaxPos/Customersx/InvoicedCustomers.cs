        public void InsertCustomer()
        {
            try
            {
                MySqlConnection connection = new MySqlConnection(this.Db.DBConnecString());
                connection.Open();
                MySqlCommand command = new MySqlCommand("INSERT INTO customersinvoiced (IdNumber, CustomerType, Name, Gender, MaxAmount, Phone, Email, Address, InvoiceStatus, RegDate, Counter, UserId) VALUES(@IdNumber, @CustomerType, @Name, @Gender, @MaxAmount, @Phone, @Email,@Address, @InvoiceStatus, @RegDate, @Counter, @UserId);", connection);
                command.Parameters.AddWithValue("@IdNumber", this.textBox1.Text.Trim());
                if (this.radioButton6.Checked)
                {
                    command.Parameters.AddWithValue("@CustomerType", "Individual");
                }
                if (this.radioButton5.Checked)
                {
                    command.Parameters.AddWithValue("@CustomerType", "Company");
                }
                if (this.radioButton1.Checked)
                {
                    command.Parameters.AddWithValue("@Gender", "Male");
                }
                if (this.radioButton2.Checked)
                {
                    command.Parameters.AddWithValue("@Gender", "Female");
                }
                if (this.radioButton4.Checked)
                {
                    command.Parameters.AddWithValue("@InvoiceStatus", 1);
                }
                if (this.radioButton3.Checked)
                {
                    command.Parameters.AddWithValue("@InvoiceStatus", 0);
                }
                command.Parameters.AddWithValue("@Name", this.textBox2.Text.Trim());
                command.Parameters.AddWithValue("@Phone", this.textBox1.Text.Trim());
                command.Parameters.AddWithValue("@Email", this.textBox4.Text.Trim());
                command.Parameters.AddWithValue("@Address", this.textBox5.Text.Trim());
                command.Parameters.AddWithValue("@MaxAmount", this.textBox6.Text.Trim());
                command.Parameters.AddWithValue("@RegDate", Program.CurrentDateTime());
                command.Parameters.AddWithValue("@Counter", Program.LogInCounter);
                command.Parameters.AddWithValue("@UserId", Program.CurrLoggedInUser.UserID);
                if (command.ExecuteNonQuery() > 0)
                {
                    MessageBox.Show("You have successfully registered the Customer", "SUCCESS", MessageBoxButtons.OK);
                }
                else
                {
                    MessageBox.Show("failed to register the Customer", "Regitrastion failed", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
                }
                connection.Close();
            }
            catch (Exception exception)
            {
                MessageBox.Show(exception.Message + exception.StackTrace, "ERROR MESSAGE", MessageBoxButtons.OK);
            }
        }

        private void LoadInvoicedCustomers()
        {
            try
            {
                this.dataGridView1.Rows.Clear();
                MySqlConnection connection = new MySqlConnection(this.Db.DBConnecString());
                connection.Open();
                MySqlCommand command = connection.CreateCommand();
                command.CommandText = "SELECT * from customersinvoiced;";
                MySqlDataReader reader = command.ExecuteReader();
                if (!reader.HasRows)
                {
                    MessageBox.Show("No invoiced Customers Found!", "MessageBox", MessageBoxButtons.OK, MessageBoxIcon.Asterisk);
                }
                else
                {
                    while (true)
                    {
                        if (!reader.Read())
                        {
                            break;
                        }
                        object[] values = new object[10];
                        values[0] = reader.GetString("IdNumber");
                        values[1] = reader.GetString("CustomerType");
                        values[2] = reader.GetString("Name");
                        values[3] = reader.GetString("Gender");
                        values[4] = reader.GetString("MaxAmount");
                        values[5] = reader.GetString("Phone");
                        values[6] = reader.GetString("Email");
                        values[7] = reader.GetString("Address");
                        values[8] = reader.GetString("InvoiceStatus");
                        values[9] = reader.GetString("RegDate");
                        this.dataGridView1.Rows.Add(values);
                    }
                }
                connection.Close();
            }
            catch (Exception exception1)
            {
                MessageBox.Show(exception1.Message, "Error Message", MessageBoxButtons.OK, MessageBoxIcon.Hand);
            }
        }

