        private void Btn_AddCustomer_Click(object sender, EventArgs e)
        {
            if (((this.textBox1.Text == "") || ((this.textBox2.Text == "") || ((this.textBox4.Text == "") || ((this.textBox5.Text == "") || (this.textBox6.Text == ""))))) || (!this.radioButton1.Checked && !this.radioButton2.Checked))
            {
                MessageBox.Show("Incomplete Customer Details", "Message Box", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }
            else if (!(this.radioButton4.Checked || this.radioButton3.Checked))
            {
                MessageBox.Show("Select Invoice Status", "Message Box", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }
            else if (!(this.radioButton1.Checked || this.radioButton2.Checked))
            {
                MessageBox.Show("Select Customer's Gender", "Message Box", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }
            else if (this.radioButton5.Checked || this.radioButton6.Checked)
            {
                this.InsertCustomer();
            }
            else
            {
                MessageBox.Show("Select Customer Type", "Message Box", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }
        }

